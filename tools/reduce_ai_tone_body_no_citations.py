CITE_PATTERNS = [
    re.compile(r"文献\s*\[[0-9,，\-—\s]+\]"),
    re.compile(r"文献\s*[0-9]+"),
    re.compile(r"\[[0-9,，\-—\s]+\]"),
]


def replace_paragraph_text(paragraph, new_text: str) -> None:
    # Keep the paragraph style and paragraph-level formatting; plain body paragraphs are
    # intentionally rewritten as single-run text to avoid preserving citation fragments.
    paragraph.text = new_text


def main() -> None:
    backup = DOCX.with_name(
        DOCX.stem + "_正文降AI修改前备份_" + datetime.now().strftime("%Y%m%d_%H%M%S") + DOCX.suffix
    )
    shutil.copy2(DOCX, backup)

    doc = Document(DOCX)
    in_refs = False
    changed = 0
    removed_cites = 0

    for paragraph in doc.paragraphs:
        text = paragraph.text.strip()
        if text == "参考文献":
            in_refs = True
        if not text or in_refs:
            continue

        for prefix, replacement in REPLACEMENTS.items():
            if text.startswith(prefix):
                replace_paragraph_text(paragraph, replacement)
                changed += 1
                text = replacement
                break

        cleaned = text
        for pattern in CITE_PATTERNS:
            cleaned = pattern.sub("", cleaned)
        cleaned = re.sub(r"\s+([，。；：、])", r"\1", cleaned)
        cleaned = re.sub(r"（\s*）", "", cleaned)
        if cleaned != text:
            replace_paragraph_text(paragraph, cleaned)
            removed_cites += 1

    doc.save(DOCX)
    print("backup", backup)
    print("rewritten_paragraphs", changed)
    print("body_citation_cleanups", removed_cites)


if __name__ == "__main__":
    main()
