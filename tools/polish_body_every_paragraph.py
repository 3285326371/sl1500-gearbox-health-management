def is_heading_or_caption(text: str) -> bool:
    if not text:
        return True
    if re.match(r"^第\d+章", text):
        return True
    if re.match(r"^\d+(?:\.\d+)*\s", text):
        return True
    if re.match(r"^[图表]\d+\.\d+", text):
        return True
    return False


def set_text(paragraph, text: str):
    paragraph.text = ""
    run = paragraph.add_run(text)
    run.font.name = "宋体"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")


def polish(text: str) -> str:
    if text in EXACT:
        return EXACT[text]
    next_text = text
    for old, new in COMMON_REPLACEMENTS:
        next_text = next_text.replace(old, new)
    next_text = next_text.replace("；", "；")
    return next_text


def main():
    shutil.copy2(SRC, OUT)
    doc = Document(OUT)
    in_body = False
    processed = 0
    changed = 0
    for paragraph in doc.paragraphs:
        text = paragraph.text.strip()
        if text == "第1章 绪论":
            in_body = True
        if text == "参考文献":
            in_body = False
        if not in_body or is_heading_or_caption(text):
            continue
        processed += 1
        next_text = polish(text)
        if next_text != text:
            set_text(paragraph, next_text)
            changed += 1

    settings = doc.settings._element
    update = settings.find(qn("w:updateFields"))
    if update is None:
        update = OxmlElement("w:updateFields")
        settings.append(update)
    update.set(qn("w:val"), "true")

    doc.save(OUT)
    checked = Document(OUT)
    body_text = []
    in_body = False
    for p in checked.paragraphs:
        t = p.text.strip()
        if t == "第1章 绪论":
            in_body = True
        if t == "参考文献":
            in_body = False
        if in_body and not is_heading_or_caption(t):
            body_text.append(t)
    joined = "\n".join(body_text)
    print(f"saved={OUT}")
    print(f"processed_body_paragraphs={processed}")
    print(f"changed_body_paragraphs={changed}")
    for term in ["能够", "本文系统", "具有一定", "测试结果表明", "该页面", "从"]:
        print(f"{term}={joined.count(term)}")
    print(f"tables={len(checked.tables)}")
    print(f"inline_shapes={len(checked.inline_shapes)}")


if __name__ == "__main__":
    main()
