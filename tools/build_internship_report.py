def clear_document(doc):
    body = doc._body._element
    sect_pr = None
    for child in list(body):
        if child.tag == qn("w:sectPr"):
            sect_pr = deepcopy(child)
        body.remove(child)
    if sect_pr is not None:
        body.append(sect_pr)


def set_defaults(doc):
    for section in doc.sections:
        section.top_margin = Cm(2.54)
        section.bottom_margin = Cm(2.54)
        section.left_margin = Cm(3.17)
        section.right_margin = Cm(3.17)
    normal = doc.styles["Normal"]
    normal.font.name = "宋体"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    normal._element.rPr.rFonts.set(qn("w:ascii"), "Times New Roman")
    normal.font.size = Pt(12)


def set_run(run, font="宋体", size=12, bold=False):
    run.font.name = font
    run._element.rPr.rFonts.set(qn("w:eastAsia"), font)
    run._element.rPr.rFonts.set(qn("w:ascii"), "Times New Roman")
    run.font.size = Pt(size)
    run.bold = bold


def add_p(doc, text="", first=True, align=None, bold=False, size=12, space_before=0, space_after=0):
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_before = Pt(space_before)
    p.paragraph_format.space_after = Pt(space_after)
    if first:
        p.paragraph_format.first_line_indent = Cm(0.74)
    if align is not None:
        p.alignment = align
    r = p.add_run(text)
    set_run(r, "宋体", size, bold)
    return p


def add_title(doc, text):
    p = add_p(doc, text, first=False, align=WD_ALIGN_PARAGRAPH.CENTER, bold=True, size=16, space_after=10)
    return p


def add_h1(doc, text):
    return add_p(doc, text, first=False, bold=True, size=14, space_before=8, space_after=4)


def add_h1_new_page(doc, text):
    p = add_h1(doc, text)
    p.paragraph_format.page_break_before = True
    return p


def add_h2(doc, text):
    return add_p(doc, text, first=False, bold=True, size=12, space_before=4, space_after=2)


