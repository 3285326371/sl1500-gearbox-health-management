def set_cell_shading(cell, fill):
    if not fill:
        return
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def set_cell_border(cell, top=None, bottom=None, left=None, right=None, inside_h=None, inside_v=None):
    tc_pr = cell._tc.get_or_add_tcPr()
    tc_borders = tc_pr.first_child_found_in("w:tcBorders")
    if tc_borders is None:
        tc_borders = OxmlElement("w:tcBorders")
        tc_pr.append(tc_borders)
    mapping = {
        "top": top,
        "bottom": bottom,
        "left": left,
        "right": right,
        "insideH": inside_h,
        "insideV": inside_v,
    }
    for edge, spec in mapping.items():
        tag = "w:" + edge
        element = tc_borders.find(qn(tag))
        if element is None:
            element = OxmlElement(tag)
            tc_borders.append(element)
        if spec is None:
            spec = {"val": "nil"}
        for key, value in spec.items():
            element.set(qn("w:" + key), str(value))


def apply_three_line_table(table):
    thick = {"val": "single", "sz": "12", "color": "000000"}
    thin = {"val": "single", "sz": "6", "color": "000000"}
    nil = {"val": "nil"}
    row_count = len(table.rows)
    for r_idx, row in enumerate(table.rows):
        for cell in row.cells:
            set_cell_border(cell, top=nil, bottom=nil, left=nil, right=nil)
            tc_pr = cell._tc.get_or_add_tcPr()
            for shd in list(tc_pr.findall(qn("w:shd"))):
                tc_pr.remove(shd)
            if r_idx == 0:
                set_cell_border(cell, top=thick, bottom=thin, left=nil, right=nil)
            elif r_idx == row_count - 1:
                set_cell_border(cell, top=nil, bottom=thick, left=nil, right=nil)


def set_cell(cell, text, bold=False, center=False):
    cell.text = ""
    p = cell.paragraphs[0]
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.line_spacing = 1.2
    r = p.add_run(str(text))
    run_font(r, "宋体", 9.5, bold)
    cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER


def add_table(doc, caption, headers, rows):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(caption)
    run_font(r, "黑体", 10.5, True)
    table = doc.add_table(rows=1, cols=len(headers))
    try:
        table.style = "Table Grid"
    except KeyError:
        pass
    for i, h in enumerate(headers):
        set_cell(table.rows[0].cells[i], h, bold=True, center=True)
    for row_data in rows:
        cells = table.add_row().cells
        for i, val in enumerate(row_data):
            set_cell(cells[i], val, center=(len(str(val)) < 12))
    apply_three_line_table(table)
    doc.add_paragraph()
    return table


