def main():
    doc = Document(TEMPLATE)
    start = next(i for i, p in enumerate(doc.paragraphs) if p.text.strip() in {"目  录", "目□□录"}) + 1
    end = next(i for i in range(start, len(doc.paragraphs)) if doc.paragraphs[i].text.strip() == "第1章 绪论")
